package com.in.tastalker.vo;

import java.util.Date;

public class GradeVO {
	private int gradeNum;
	private int gradeGrade;
	private Date gradeReg;
	private String userId;
	public int getGradeNum() {
		return gradeNum;
	}
	public void setGradeNum(int gradeNum) {
		this.gradeNum = gradeNum;
	}
	public int getGradeGrade() {
		return gradeGrade;
	}
	public void setGradeGrade(int gradeGrade) {
		this.gradeGrade = gradeGrade;
	}
	public Date getGradeReg() {
		return gradeReg;
	}
	public void setGradeReg(Date gradeReg) {
		this.gradeReg = gradeReg;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	

}
